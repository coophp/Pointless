<?php

class pointless_gen_all extends CLI {
	public function __construct() {
		parent::__construct();
	}
	
	public function Run() {
		require_once CORE_LIB . 'compress.php';
		require_once CORE_LIB . 'generator.php';
		
		// Clean static pages
		$clean = new pointless_gen_clean();
		$clean->run();
		
		if(!file_exists(HTDOCS))
			mkdir(HTDOCS);
		
		$this->rCopy(RESOURCE, HTDOCS);
		
		if(CNAME !== NULL) {
			$handle = fopen(HTDOCS . 'CNAME', 'w+');
			fwrite($handle, CNAME);
			fclose($handle);
		}
		
		if(!file_exists(HTDOCS . 'README')) {
			$handle = fopen(HTDOCS . 'README', 'w+');
			fwrite($handle, 'Generated by Pointless');
			fclose($handle);
		}
		
		$compress = new compress();
		$compress->js();
		$compress->css();
		
		$generator = new generator();
		$generator->run();
	}
	
	private function rCopy($src, $dest) {
		if(is_dir($src)) {
			if(!file_exists($dest))
				mkdir($dest, 0755, TRUE);
			$handle = @opendir($src);
			while($file = readdir($handle))
				if($file != '.' && $file != '..' && $file != '.git')
					$this->rCopy($src . DIRECTORY_SEPARATOR . $file, $dest . DIRECTORY_SEPARATOR . $file);
			closedir($handle);
		}
		else
			copy($src, $dest);
	}
}
